// Default language
export let currentLang = "en"

// Simple environment detection - runs only once when module loads
const isBrowser = typeof window !== "undefined" && typeof window.localStorage !== "undefined"

export function setLang(lang: string) {
	currentLang = lang
	if (isBrowser) {
		localStorage.setItem("lang", lang)
	}
}

export function getLang() {
	if (isBrowser) {
		// In browser - try to get from localStorage first
		const storedLang = localStorage.getItem("lang")
		return storedLang || currentLang
	}
	// In server environment, return default language
	return currentLang
}

export function t(key: string): string {
	const lang = getLang() as "fr" | "en" | "ch" | "ta" | "ar"
	const langObj = translations[lang] as Record<string, string>
	return langObj[key] || `${key} ✘ (Missing trad) ✘`
}
